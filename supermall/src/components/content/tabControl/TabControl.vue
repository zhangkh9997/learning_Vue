<template>
  <div class="tab-control">
    <!--  因为只是文字不同，而不是功能不同，所以不需要使用插槽-->
    <div v-for="(item,index) in titles"
         :key="index"
         class="tab-control-item"
         :class="{active:index===currentIndex}"
         @click="itemClick(index)">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
    export default {
        name: "TabControl",
        props:{
            titles: {
                type: Array,
                default(){
                    return []
                }
            }
        },
        data(){
            return {
                currentIndex: 0
            }
        },
        methods:{
            itemClick(index){
                this.currentIndex = index
            }
        }
    }
</script>

<style scoped>
  .tab-control {
    display: flex;
    text-align: center;
